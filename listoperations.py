#List Operations:-
#1. concatenation:-
list1 = [1,2,3]
list2 = [4,5]
list3 =list1+list2
print(list3)                  #output = [1,2,3,4,5]
#2.Repeatition:-
list = [1,2,3]
Newlist = list*4
